function changePlaceholderTexts(language) {
  // Get the form fields by their name attributes
  const nameField = document.querySelectorAll('[name="name"]');
  const emailField = document.querySelectorAll('[name="email"]');
  const messageField = document.querySelectorAll('[name="message"]');
  const phone =  document.querySelector('[name="phone"]');
  const country = document.querySelector('[name="country"]');
  const city = document.querySelector('[name="city"]');

  // Update the placeholder text for each field
  nameField.forEach((element)=>{
     element.placeholder=placeholderTexts[language].name;
  })
  emailField.forEach((element)=>{
    element.placeholder=placeholderTexts[language].email;
  })
  messageField.forEach((element)=>{
  element.placeholder=placeholderTexts[language].message;
  })
  phone.placeholder = placeholderTexts[language].phone;
  country.placeholder = placeholderTexts[language].country;
  city.placeholder = placeholderTexts[language].city;
}

function translate(currentLanguage){
  const elements = document.querySelectorAll('[data-translate]');
  // loop through the elements and update the text
  elements.forEach(function(element) {
    element.innerHTML = translations[currentLanguage][element.getAttribute('data-translate')];
  });
  changePlaceholderTexts(currentLanguage);
}

translate("am")

const languageLinks = document.querySelectorAll(".language");
 for (let languageLink of languageLinks) {
    languageLink.addEventListener("click", function(event) {
    event.preventDefault();
    const selectedLanguage = languageLink.getAttribute("data-language");
    switch (selectedLanguage) {
       case "en":
          translate('en')
          break;
       case "am":
          translate('am')
          break;
       case "ru":
          translate('ru')
          break;
    }
  });
}
